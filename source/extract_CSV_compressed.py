def main()->bool:
    log_prefix = '[ext-apache_parquet | main]'
    try:
        if init():
            logging_msg(f"{log_prefix} ALL OK")

        logging_msg(f"{log_prefix} END")
        return True
    
    except Exception as e:
        logging_msg(f"{log_prefix} Error: {e}", 'CRITICAL')
        return False
